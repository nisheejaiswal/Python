
# Python Bitwise Operators

x = 5
y = 6

# & Operator (AND)
print(x & y)

# | Operator (OR)
print(x | y)

# ^ Operator (XOR)
print(x ^ y)

# ~ Operator (NOT)
print(~x)

# << Operator (Zero fill left shift)
a = 6
b = 2
print(a << b)

# >> Operator (Signed right shift)
a = 6
b = 2
print(a >> b)
