
# Tuple - ordered and unchangeable
thistuple = ("apple", "banana", "cherry")
print(thistuple)

# Access tuple Items
thistuple = ("apple", "banana", "cherry")
print(thistuple[1])

# Change Tuple Values - Once a tuple is creates, we cannot change its values.

# Loop Through a Tuple
thistuple = ("apple", "banana", "cherry")
for x in thistuple:
    print(x)

# Tuple Length
print(len(thistuple))

# Add Items - We cannot add items

# Remove Items
thistuple = ("apple", "banana", "cherry")
del thistuple

# The tuple() Constructor
thistuple = tuple(("apple", "banana", "cherry"))
print(thistuple)





